//program to demonstrate on unboxing
/*
 * unboxing - conversion of  object type to primitive 
 * example - character to char
 */

package com.tnsif.wrapperclass;

public class UnBoxing {

	public static void main(String[] args) {
		Character c = 'A';
		//unboxing
		char ch = c;
		System.out.println(ch);

	}

}
